# Time Limit Exceeded
class Solution:
    def shortestPath(self, grid: List[List[int]], k: int) -> int:

        m = len(grid)
        n = len(grid[0])

        ans = inf
        visited = set()

        q = [(0, 0, 0, 0)]
        while q:
            x, y, moves, eliminations = q.pop(0)
            visited.add((x, y))

            if x == n-1 and y == m-1 and eliminations <= k:
                ans = min(ans, moves)

            for nx, ny in [(x, y-1), (x+1, y), (x, y+1), (x-1, y)]:
                if nx < 0 or nx >= n or \
                   ny < 0 or ny >= m or \
                   (nx, ny) in visited:
                    continue

                new_moves = moves + 1
                new_eliminations = eliminations + \
                    1 if grid[ny][nx] == 1 else eliminations
                q.append((nx, ny, new_moves, new_eliminations))

        return ans if ans != inf else -1


